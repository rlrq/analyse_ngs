## summarise reads
summarise_reads_multi(mkpath(dir_identity, paste0(prefix, ".identity.txt")),
                      df.meta, args$dir_derep, args$dir_blast,
                      fout_ontarget = mkpath(dir_identity, paste0(prefix, ".identity-ontarget.txt")))

